public class TiposVariavel {

    public static void main(String[] args) {  //main é estrutura prncipal do projeto
        System.out.println("Hello world");
        System.out.println(2 + 2);

        var login = "Hello"; //Definição de uma variável
        System.out.println(login);

        var password = "12345";
        System.out.println(password);

        // tipos de dados
        String name; //string
        name = "Ezequiel";
        System.out.println(name);

        int number; // Número inteiro
        number = 12;
        System.out.println(number);

        double percent; // Número fracionados
        percent = 12.33;
        System.out.println(percent);

        boolean isDriver; // Estado verdadeiro ou faloso
        isDriver = false;
        System.out.println(isDriver);



    }

}

