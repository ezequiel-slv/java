package com.ezequiel;

public class TipoPrimitivo2 {
    public static void main(String[] args) {
        //tipos primitivos

        boolean save = true; //define dois estados True e false (condições, iniciar e terminar loops)
        byte a = 127; // armazena até 127 e 8bits
        char b = 'b'; //Representação do proprio caracter (trabalha apenas com uma letra)
        short c = 30000; //Semelhante ao int, no entanto utiliza menos espaço
        int d = 300000000; //números grandes
        long e = 3088888888888888000L; // aloca uma quantidade maior que o int
        float f = 2.5f; //armazena números fracionados
        double g = 21.45; // armazena números fracionados (aloca mais que float)

    }
}
